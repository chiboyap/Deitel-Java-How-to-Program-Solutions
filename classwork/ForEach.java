package exercises;

public class ForEach {

	public static void main(String[] args) {
	int[] array = new int[] {12,12,23,34,4,3,32,22,22} ;
	// for each element in(:) reference array loop through ,no index here to assign
	//mostly used for arrays 
	for(double i:array) {
		System.out.println("the loop " + i);
	}

	}

}
